			</p>
			<ol type="a">
				<h3><li>Trabalho Completo</li></h3>
				<p align="justify">
					O trabalho completo é constituído de um único texto, que pode ser organizado, por no máximo, quatro autores, além do professor orientador, quando houver.  
Todo acadêmico de graduação que desejar  submeter um trabalho, obrigatoriamente, deverá ter um professor orientador. Todo trabalho realizado sob orientação deve indicar o professor orientador, o qual, no âmbito do evento, não é considerado autor, coautor, proponente ou responsável.
O trabalho poderá apresentar  pesquisas teóricas e/ou empíricas concluídas ou em andamento, e deverá inserir-se em um dos quatro eixos do evento.  
A apresentação do trabalho se dará em forma de comunicação oral, com tempo de 15 minutos, em um grupo de até 10 trabalhos, com discussão ao final das apresentações. 

				</p>
				<h3><li>Oficina Pedagógica</li></h3>
				<p align="justify">
					A oficina pedagógica, atividade de caráter eminentemente prático, deve constituir-se como um espaço de socialização de saberes e fazeres com vistas ao aprimoramento da formação e atuação docentes. Tem o objetivo de oportunizar aos proponentes e participantes o desenvolvimento de competências e habilidades para atuar na sua vida profissional. 
Tal como os trabalhos, as oficinas devem se inserir em um dos quatro eixos temáticos do evento. 
As oficinas terão duração de 2 a 3 horas, podendo ser propostas por alunos, professores ou outros profissionais que desenvolvam atividades relacionadas à educação. As propostas podem ser desenvolvidas por até cinco pessoas, entre as quais uma deve ser indicada como proponente, que responderá pela proposta junto à organização do evento. 
				</p>
