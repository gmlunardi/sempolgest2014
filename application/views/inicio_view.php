				e superior. Essa integração vai muito além das reflexões do espaço escolar e universitário; implica, antes de tudo, criar possibilidades de se analisarem questões e desafios considerados
				fundamentais no âmbito da gestão educacional, em face das demandas que a educação enfrenta no contexto de uma sociedade em transformação. Compreender significativamente a relação sociedade e 
				educação, é ir além do contexto micro educacional, o que sugere uma análise mais profunda de seus contextos culturais, econômicos, históricos e políticos, no intuito de apontar necessidades e 
				possibilidades.
