			<h2>Oficinas Pedagógicas</h2>
			
			<p align="justify">
				Para submeter uma proposta de oficina, o proponente deve preencher e enviar o formulário de inscrição correspondente, 
				no qual precisa informar, entre outras coisas: a temática, o nome do proponente e dos demais responsáveis, 
				a descrição da atividade (Resumo) e os recursos necessários 
				para sua execução.
			</p>
			<p align="justify">
				<b>Resumo:</b> Deve conter de 200 a 500 palavras e indicar os OBJETIVOS da atividade, a FUNDAMENTAÇÃO TEÓRICA da proposta, 
				a METODOLOGIA, os RESULTADOS ESPERADOS, as REFERÊNCIAS BIBLIOGRÁFICAS e as PALAVRAS-CHAVES. O resumo deve ser suficientemente claro, de modo 
				a permitir uma visão geral da atividade a ser desenvolvida.
			</p>
			<p align="justify">
				<b>Recursos necessários:</b> Todas as salas que abrigarão oficinas dispõem de quadro branco, carteiras e cadeiras, computador 
				com acesso à internet e data show. Caso haja necessidade de outros equipamentos ou materiais específicos 
				para o desenvolvimento da oficina, estes devem ser indicados, com as devidas especificações e quantidades necessárias para cada 
				oficina, ou seja, para a totalidade do grupo a ser atendido, levando em conta o número de vagas ofertadas pelo proponente.
			</p>
