			<h2>Trabalhos Completos</h2>
			<p align="justify">
				Devido aos critérios de publicação definidos pela Comissão Organizadora, em atendimento às normas da ABNT e, também, 
				para o registro de ISBN no CD dos Anais, orientamos os autores a submeterem trabalhos inéditos. Para a submissão de trabalhos 
				completos, deve-se seguir a formatação e as orientações expressas abaixo.
			</p>

			<ol type="a">
				<li>
					margem superior e esquerda 3 cm e margem inferior e direita 2 cm;
				</li>
				<li>
					título do trabalho em letra maiúscula, centralizado, negritado em fonte Arial tamanho 12, espaço 1,5 entre linhas, em no máximo três linhas;
				</li>
				<li>
					nomes completos dos autores (conforme ficha de inscrição), em fonte Arial tamanho 12, alinhados a direita;
				</li>
				<li>
					nota de rodapé correspondente a cada autor, em fonte Arial tamanho 10 e espaço simples, contendo formação, instituição e e-mail;
				</li>
				<li>
					indicação do apresentador do trabalho e do orientador, quando houver; 
				</li>
				<li>
					número e nome do eixo temático abaixo do nome dos autores; 
				</li>
				<li>
					 resumo em bloco único, fonte Arial tamanho 10, espaço simples, sem recuo de parágrafo, entre 250 e 350 palavras (incluindo as palavras-chave); abaixo do resumo devem constar no mínimo três e no máximo cinco palavras-chave separadas por ponto final; 
				</li>
				<li>
					corpo do texto (introdução contendo problema e respectivo(s) objetivo(s) da pesquisa, fundamentação teórica, metodologia, considerações finais e referências) em fonte Arial tamanho 12, espaçamento 1,5 cm, com recuo de parágrafo de 1 cm; 
				</li>
				<li>
					 extensão do texto: entre 8 a 12 páginas incluindo o resumo e as referências; 
				</li>
				<li>
					citações (diretas e indiretas) e referências apresentadas conforme normas da Associação Brasileira de Normas Técnicas – ABNT;
				</li>
				<li>
					títulos das seções do trabalho numerados, em negrito, sendo a primeira letra maiúscula; 
				</li>
				<li>
					entre os parágrafos do texto não pode haver linhas em branco, ou seja, não deve haver espaçamento entre parágrafos. Apenas entre uma seção e outra (deixar uma linha em branco); 
				</li>
				<li>
					será permitida a utilização de figuras, gráficos e tabelas, desde que identificados de acordo com as normas da Associação Brasileira de Normas Técnicas – ABNT; 
				</li>
				<li>
					os trabalhos deverão ser salvos em dois arquivos em versão Word (formato DOCX), identificados de acordo com as seguintes orientações: 
				</li>
